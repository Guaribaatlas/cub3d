#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb++;
		nb = -nb;
		if (nb == 2147483647)
		{
			ft_putchar('2');
			nb = 147483647;
		}
		nb++;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	ft_putchar(nb % 10 + '0');
}
